public class AritmatikaDemo {//class aritmatikaDemo dari folder pertemuan1
     public static void main (String[] args){// fungsi utama dari sebuah method
         int i=5;//inisialisasi type Data INTEGER dengan variable I yang bernilai 5
         int j=4;//inisialisasi type Data INTEGER dengan variable j yang bernilai 4
         double x=2.50;//inisialisasi type Data double  dengan variable x yang bernilai 2.50
         double y=7.25; //inisialisasi type Data double  dengan variable y  yang bernilai 7.25
         System.out.println("Variable Value...."); //mencetak dilayar Variable Value....
         System.out.println("i = " +i); //mencetak I sama dengan nilai dari integer  variable I saama dengan 5
        //mencetak j sama dengan nilai dari integer  variable j saama dengan 4
         System.out.println("j = " +j); 
       // mencetak x sama dengan nilai dari integer  variable x saama dengan 2.50
         System.out.println("x = " +x);
       // mencetak y sama dengan nilai dari integer  variable y saama dengan 7,25
         System.out.println("y = " +y);
         
         //penjumlahan angka
         System.out.println("Adding......."); // mencetak dilayar variable Adding
         System.out.println("i + j " +(i+j));//mencetak i+j dan menghasilkan penjumlahan i+j
         System.out.println("x + y " +(x+y)); //mencetak x+y dan menghasilkan penjumlahan x+y
         
         //pengurangan angka
         System.out.println("Subsstrating....."); // mencetak dilayar variable Subsstrating
         System.out.println("i - j " +(i-j)); //mencetak i-j dan menghasilkan pengurangani-j
         System.out.println("x - y " +(x-y)); //mencetak x-y dan menghasilkan pengurangan x-y

         
         //perkalian angka
         System.out.println("Multipliying....."); // mencetak dilayar variable Multipliying
         System.out.println("i * j " +(i*j)); //mencetak i*j dan menghasilkan perkalian i*j
         System.out.println("x * y " +(x*y)); //mencetak x*y dan menghasilkan perkalian x*y
         //pembagian angka
         System.out.println("Dividing........"); // mencetak dilayar variable Dividding
         System.out.println("i / j " +(i/j)); //mencetak i/j dan menghasilkan pembagian i/j
         System.out.println("x / y " +(x/y)); //mencetak x/y dan menghasilkan pembagian x/y
         //menghitung hasil modulus dari pembagian
         System.out.println("Computing Remainder......"); 
        // mencetak dilayar variable Computing Remainder
         System.out.println("i % j " +(i%j)); //mencetak i%j dan menghasilkanmodulus dari pembagian i%j
         System.out.println("x % y " +(x %y)); 
        //mencetak x%y dan menghasilkanmodulus dari pembagian x%y
         
         //tipe penggabungan
         System.out.println("Mixing Type....."); // mencetak dilayar variable Mixing Type
         System.out.println("j + y " +(j+y)); //mencetak j+y dan menghasilkan penggabungan j+y
         System.out.println("i * x " +(i*x)); //mencetak i*x dan menghasilkan penggabungan i*x 
     }
}
